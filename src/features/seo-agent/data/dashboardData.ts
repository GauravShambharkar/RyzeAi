const bandFromValue = (
  value: number,
  good: number,
  poor: number
): VitalBand => {
  if (value <= good) return "good";
  if (value <= poor) return "needs-improvement";
  return "poor";
};

export const getDashboardData = (domain: string): DashboardData => {
  const rnd = mulberry32(hashSeed(domain));

  // Core Web Vitals — realistic ranges.
  const lcpMs = intBetween(rnd, 1600, 4200);
  const inpMs = intBetween(rnd, 80, 380);
  const clsRaw = Math.round(rnd() * 0.35 * 100) / 100;

  const webVitals: WebVital[] = [
    {
      id: "lcp",
      label: "Largest Contentful Paint",
      valueLabel: `${(lcpMs / 1000).toFixed(1)}s`,
      target: "≤ 2.5s",
      band: bandFromValue(lcpMs, 2500, 4000),
      helper: "How fast the main content paints.",
    },
    {
      id: "inp",
      label: "Interaction to Next Paint",
      valueLabel: `${inpMs}ms`,
      target: "≤ 200ms",
      band: bandFromValue(inpMs, 200, 500),
      helper: "Responsiveness to taps and clicks.",
    },
    {
      id: "cls",
      label: "Cumulative Layout Shift",
      valueLabel: clsRaw.toFixed(2),
      target: "≤ 0.10",
      band: bandFromValue(clsRaw, 0.1, 0.25),
      helper: "How much the layout shifts while loading.",
    },
  ];

  // Keywords — pick 10 distinct from the pool with believable ranking curves.
  const keywordSlice = [...KEYWORD_POOL]
    .sort(() => rnd() - 0.5)
    .slice(0, 10);
  const keywords: Keyword[] = keywordSlice
    .map((keyword, i) => {
      const position = intBetween(rnd, 1 + i * 2, 12 + i * 3);
      const delta = intBetween(rnd, -4, 6);
      const volume = [110, 210, 390, 580, 880, 1200, 1900, 2600, 3400, 4800][
        intBetween(rnd, 0, 9)
      ];
      return { keyword, position, delta, volume };
    })
    .sort((a, b) => a.position - b.position);

  // Issues — shuffle + assign realistic counts by severity.
  const issues: Issue[] = [...ISSUE_POOL]
    .sort(() => rnd() - 0.5)
    .slice(0, 6)
    .map((base) => ({
      ...base,
      count:
        base.severity === "critical"
          ? intBetween(rnd, 1, 8)
          : base.severity === "warning"
            ? intBetween(rnd, 2, 24)
            : intBetween(rnd, 5, 60),
    }))
    .sort((a, b) => {
      const order: Record<IssueSeverity, number> = {
        critical: 0,
        warning: 1,
        info: 2,
      };
      return order[a.severity] - order[b.severity];
    });

  // Overall score — inverse-weighted by band severity + issue severity.
  const vitalPenalty = webVitals.reduce((sum, v) => {
    if (v.band === "good") return sum;
    if (v.band === "needs-improvement") return sum + 6;
    return sum + 12;
  }, 0);
  const issuePenalty = issues.reduce((sum, i) => {
    if (i.severity === "critical") return sum + 8;
    if (i.severity === "warning") return sum + 3;
    return sum + 1;
  }, 0);
  const rawScore = Math.max(30, Math.min(99, 95 - vitalPenalty - issuePenalty));
  const scoreBand: DashboardData["scoreBand"] =
    rawScore >= 80 ? "strong" : rawScore >= 60 ? "fair" : "needs-work";

  const scannedAgo = pick(rnd, [
    "2 minutes ago",
    "18 minutes ago",
    "1 hour ago",
    "4 hours ago",
  ]);
  const indexedPages = intBetween(rnd, 38, 340);
  const crawlErrors = intBetween(rnd, 0, 12);

  return {
    score: rawScore,
    scoreBand,
    scannedAgo,
    indexedPages,
    crawlErrors,
    webVitals,
    keywords,
    issues,
  };
};

// Turn a clicked dashboard item into a natural-language chat prompt scoped
// to the active domain. Keeps the "AI recommends fixes" UX centralized.

export const promptForVital = (vital: WebVital, domain: string) =>
  `My ${vital.label} on ${domain} is ${vital.valueLabel} (target: ${vital.target}) — currently flagged as "${vital.band}". Walk me through the most impactful fixes, in priority order.`;

export const promptForKeyword = (kw: Keyword, domain: string) => {
  const direction = kw.delta > 0 ? `up ${kw.delta}` : kw.delta < 0 ? `down ${Math.abs(kw.delta)}` : "flat";
  return `For "${kw.keyword}" on ${domain} I'm ranking at position ${kw.position} (${direction} this week, search volume ~${kw.volume}/mo). What's the fastest realistic path to the top 3?`;
};

export const promptForIssue = (issue: Issue, domain: string) =>
  `On ${domain} I have ${issue.count} instance${issue.count === 1 ? "" : "s"} of "${issue.title}" (${issue.severity}). ${issue.detail} How should I fix this?`;
