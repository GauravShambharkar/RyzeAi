// Deterministic mock data for the Dashboard. A given domain string always
// produces the same numbers, so the UI never flickers across re-renders.
// Swap with real integrations later.

export type VitalBand = "good" | "needs-improvement" | "poor";

export type WebVital = {
  id: "lcp" | "inp" | "cls";
  label: string;
  valueLabel: string;
  target: string;
  band: VitalBand;
  helper: string;
};

export type Keyword = {
  keyword: string;
  position: number;
  delta: number;
  volume: number;
};

export type IssueSeverity = "critical" | "warning" | "info";

export type Issue = {
  id: string;
  title: string;
  severity: IssueSeverity;
  count: number;
  detail: string;
  category: "technical" | "content" | "meta" | "links";
};

export type DashboardData = {
  score: number;
  scoreBand: "strong" | "fair" | "needs-work";
  scannedAgo: string;
  indexedPages: number;
  crawlErrors: number;
  webVitals: WebVital[];
  keywords: Keyword[];
  issues: Issue[];
};

// Tiny deterministic PRNG seeded from a string. Good enough for mock data —
// we just need stability, not cryptographic quality.
const hashSeed = (s: string) => {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
};

const mulberry32 = (seed: number) => () => {
  let t = (seed = (seed + 0x6d2b79f5) >>> 0);
  t = Math.imul(t ^ (t >>> 15), t | 1);
  t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
  return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
};

const pick = <T>(rnd: () => number, list: readonly T[]): T =>
  list[Math.floor(rnd() * list.length)];

const intBetween = (rnd: () => number, min: number, max: number) =>
  Math.floor(rnd() * (max - min + 1)) + min;

